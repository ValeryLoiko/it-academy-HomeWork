package kompleksNumber;

public class View {
    public void printSum(Numbers a, Numbers b, Numbers c){
        System.out.println(a + "i" + " + " + b + "i" + " = " + c + "i");
    }

    public void printSubt(Numbers a, Numbers b, Numbers c){
        System.out.println(a + "i" + " - " + b + "i" + " = " + c + "i");
    }
}
